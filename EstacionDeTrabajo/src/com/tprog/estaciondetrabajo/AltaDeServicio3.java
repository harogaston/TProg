	private boolean isWhiteSpace(String s) {
		return (s.matches("^\\s*$") || s.matches("^\\s.*"));
	}
	
    private void buttonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSiguienteActionPerformed
		String nombreServicio = textPaneNombreServicio.getText();
		String error = "";
		boolean ok1 = !isWhiteSpace(nombreServicio);
		boolean ok2 = ((ok1) && (ctrlProductos.idServicioDisponible(nombreServicio)));
		if (ok2) {
			DTMinServicio dtmServicio = new DTMinServicio(proveedor, nombreServicio);
			ctrlProductos.seleccionarServicio(dtmServicio);
			AltaDeServicio4 as4 = new AltaDeServicio4(this, ctrlProductos);
			this.setVisible(false);
			getParent().add(as4);
			as4.setLocation(this.getLocation());
			as4.setVisible(true);
		} else {
			if (!ok1) {
				error = "El nombre de servicio ingresado no es v√°lido.";
			} else if (!ok2) {
				error = "Ya existe un Servicio con ese nombre.";
			}
			JOptionPane.showMessageDialog(this, error, "Alta de Servicio", JOptionPane.INFORMATION_MESSAGE);
		}
    }//GEN-LAST:event_buttonSiguienteActionPerformed

