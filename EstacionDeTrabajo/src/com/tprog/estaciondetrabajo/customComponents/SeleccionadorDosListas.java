    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        List selected = lista1Interfaz.getSelectedValuesList();
        Iterator it1 = selected.iterator();
        while (it1.hasNext()){
            String actual = (String) it1.next();
            Iterator it2 = lista1.iterator();
            while (it2.hasNext()) {
                if (it2.next().equals(actual)) {
                    it2.remove();
                }
            }
            lista2.add(actual);
        }
        lista1Interfaz.updateUI();
        lista2Interfaz.updateUI();
    }//GEN-LAST:event_addButtonActionPerformed

